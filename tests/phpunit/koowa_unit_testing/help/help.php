Usage:   testkoowa [<TestName>] [options]
         When no test name is given, all tests will be executed

Example: testkoowa KFactoryTest

Option:  -h    This help
         -v    Version info
         -V    Verbose

Before you start:
  Install PHPUnit v3.2 or later
  Install Joomla! 1.5
  Install the Koowa plugin
  Copy the file 'testkoowa' and the folder 'koowa_unit_testing' to the Joomla! root
  Make 'testkoowa' executable

